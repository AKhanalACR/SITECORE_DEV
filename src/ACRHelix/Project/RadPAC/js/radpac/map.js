function generateAreas(d) {
	var areaData = {};
	for (var state in d) {
		areaData[state] = {
			text: {
				content: state,
				attrs: {
					"font-size": 16,
					"font-weight": 700,
					fill: "#edeff1"
				},
				position: checkState(state) ? 'right' : 'inner',
				margin: checkState(state) ? 40 : 0,
			},
        	stateId: d[state],
					tooltip: {content: "<div style=\"font-size:12px; font-weight: bold;\">"+ d[state].name +"</div>"}
		}
	}
	return areaData;
}
